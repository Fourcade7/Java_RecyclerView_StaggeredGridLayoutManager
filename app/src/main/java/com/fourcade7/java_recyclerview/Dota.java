package com.fourcade7.java_recyclerview;

public class Dota {

    int imagehero;
    String namehero;

    public Dota(int imagehero, String namehero) {
        this.imagehero = imagehero;
        this.namehero = namehero;
    }

    public int getImagehero() {
        return imagehero;
    }

    public void setImagehero(int imagehero) {
        this.imagehero = imagehero;
    }

    public String getNamehero() {
        return namehero;
    }

    public void setNamehero(String namehero) {
        this.namehero = namehero;
    }
}
